#A:
# In Python, the dir() function is used to list the names of the attributes and methods
# of an object. When you pass an object to dir(), it returns a list of the attributes
# and methods associated with that object.
# This will output a list of the attributes and methods that belong to the string
# object message. These include all the built-in methods available for strings,
# such as upper(), lower(), strip(), and many others.

